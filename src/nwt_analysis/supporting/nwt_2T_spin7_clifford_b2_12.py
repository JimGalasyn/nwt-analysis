def main():
    print("=" * 72)
    print(" b2.12  --  2T subset Spin(7) via octonion Clifford algebra")
    print("=" * 72)

    c = build_oct_tensor()
    L = [L_matrix(i, c) for i in range(8)]   # L[0] = identity (L_1 = identity)
    # Actually L_0 = L_{e_0} = L_1 = identity (since e_0 = 1).
    # Use L[1] through L[7] for the 7 imaginary gammas.

    # --- (1) Clifford relations {L_i, L_j} = -2 delta_ij I -------------
    print("\n[1] Verify Clifford relations  L_i L_j + L_j L_i = -2 delta_ij I")
    max_err = 0.0
    all_ok = True
    for i in range(1, 8):
        for j in range(1, 8):
            anticomm = L[i] @ L[j] + L[j] @ L[i]
            expected = -2.0 * (1 if i == j else 0) * np.eye(8)
            err = np.abs(anticomm - expected).max()
            max_err = max(max_err, err)
            if err > 1e-10:
                all_ok = False
    print(f"    max |L_i L_j + L_j L_i - (-2 delta_ij) I| = {max_err:.3e}")
    print(f"    Cl(0, 7) relations: {'PASS' if all_ok else 'FAIL'}")
    assert all_ok, "Octonion L_i do not satisfy Clifford relations."

    # --- (2) Quaternion structure of bivectors --------------------------
    I_ = L[1] @ L[2]   # "i"
    J_ = L[1] @ L[3]   # "j"
    K_ = L[2] @ L[3]   # "k"
    print("\n[2] Bivectors L_1 L_2, L_1 L_3, L_2 L_3 form a quaternion algebra")
