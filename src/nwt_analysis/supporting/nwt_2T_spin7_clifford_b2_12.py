# =========================================================================
# Octonion multiplication tensor and left-multiplication operators L_i.
# =========================================================================

def build_oct_tensor() -> np.ndarray:
    """Return c[i,j,k] such that e_i * e_j = sum_k c[i,j,k] e_k.
    Indexing: e_0 = 1 (real axis), e_1..e_7 = imaginary basis.
    """
    c = np.zeros((8, 8, 8))
    c[0, 0, 0] = 1
    for i in range(1, 8):
        c[0, i, i] = 1
        c[i, 0, i] = 1
        c[i, i, 0] = -1
    for trip in FANO_TRIPLES:
        i, j, k = trip
        c[i, j, k] = 1; c[j, i, k] = -1
        c[j, k, i] = 1; c[k, j, i] = -1
        c[k, i, j] = 1; c[i, k, j] = -1
    return c


def L_matrix(i: int, c: np.ndarray) -> np.ndarray:
    """8x8 matrix of left-multiplication by e_i on octonions.
    (L_i x)_a = sum_b c[i, b, a] x_b,  so  L_i[a, b] = c[i, b, a].
    """
    return c[i, :, :].T  # transpose: L_i[a,b] = c[i, b, a]


